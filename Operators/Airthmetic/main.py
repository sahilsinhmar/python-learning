# so airthmetic operators are -> + - * / %  // **


# + plus operator

print(1 + 2)  # 3
print(1.5 + 3)  # 4.5 if one input is float then result will be float

# for strings

print("sahil" + "sinhmar")  # sahilsinhmar

# print("sahi" + 5)  # type error -> both input should be string type

print("sahil" + "5")  # sahil5

### multiply *

print(3 * 2)  # 6

# for string it will repeat the string

print(3 * "sahil")  # sahilsahilsahil

## divison operator /

print(4 / 2)  # 2.0 float type always

## floor division operator //

print(4 // 2)  # 2 int type
print(4 // 2.0)  # 2.0 float type as one arg is in float type


## Power operator **

print(2**2)  # 4
print(2**2.0)  # 4.0 float type cuz of 2.0
