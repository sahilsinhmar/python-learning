# there are  like three types and or and not

# and -> for boolean types return true if both are true

t = True
f = False

print(t and t)  # True
print(t and f)  # False
print(f and f)  # False


### Or -> return true if one of the arg is true

print(t or f)  # True
print(t or t)  # True
print(f or t)  # True


### NOt return false if true and vice versa

print(not t)  # False
print(not f)  # True
