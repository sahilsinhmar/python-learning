# Mathematical Operator


l = [1, 2, 3, 4, "sahil"]
l2 = [1, 2, 3, 4, "sahil"]
l1 = [5, 6, 7, 8, "sinhmar"]

# 1. + operator

print(l + l1)  # [1, 2, 3, 4, 'sahil', 5, 6, 7, 8, 'sinhmar']

## note--> both should be list otherwise error

# Repeation *

# print(l * l1) # error both list

print(l * 2)  # [1, 2, 3, 4, 'sahil', 1, 2, 3, 4, 'sahil']


## Equality Operator

print(l == l1)  # False

print(l == l2)  # True


# Relational Operator
